
package com.eazytec.exceptions;


/**
 * Runtime exception that is the superclass of all exceptions.
 * 
 * @author madan
 */
public class EazyBpmException extends RuntimeException {

  private static final long serialVersionUID = 1L;

  public EazyBpmException(String message, Throwable cause) {
    super(message, cause);
  }

  public EazyBpmException(String message) {
    super(message);
  }
}
