package com.eazytec.util;

import java.util.List;
import java.util.Map;

/**
 * Created by min on 2016/6/16.
 */
public class PageResultBean {

    private int total;	//总页数
    private int page;	//当前页
    private int records;	//查询出的记录数
    private List<PageResultRow> rows; //包含实际数据的数组
    private Map<Object,Object> userdata; //user data

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

    public int getPage() {
        return page;
    }

    public void setPage(int page) {
        this.page = page;
    }

    public int getRecords() {
        return records;
    }

    public void setRecords(int records) {
        this.records = records;
    }

    public List<PageResultRow> getRows() {
        return rows;
    }

    public void setRows(List<PageResultRow> rows) {
        this.rows = rows;
    }

    public Map<Object, Object> getUserdata() {
        return userdata;
    }

    public void setUserdata(Map<Object, Object> userdata) {
        this.userdata = userdata;
    }
}

