package com.eazytec.common.util;

import java.util.Comparator;

import com.eazytec.model.MetaTableColumns;

public class MetaTableColumnComparator implements Comparator<MetaTableColumns> {

	public int compare(MetaTableColumns from, MetaTableColumns to) {
		Integer orderNoFrom = from.getOrderNum();
		Integer orderNoTo = to.getOrderNum();
		return orderNoFrom.compareTo(orderNoTo);
	}

}
