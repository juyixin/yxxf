package com.licensekit;
public class GridBagConstraintsD extends java.awt.GridBagConstraints
{
    public GridBagConstraintsD(int aGridX,
        int aGridY,
        int aGridWidth,
        int aGridHeight,
        double aWeightX,
        double aWeightY,
        int anAnchor,
        int aFill,
        java.awt.Insets anInset,
        int anIpadX,
        int anIpadY) {
        
        gridx = aGridX;
        gridy = aGridY;
        gridwidth = aGridWidth;
        gridheight = aGridHeight;
        weightx = aWeightX;
        weighty = aWeightY;
        anchor = anAnchor;
        fill = aFill;
        insets = anInset;
        ipadx = anIpadX;
        ipady = anIpadY;
        
    }
}
        
        
    
        
        
